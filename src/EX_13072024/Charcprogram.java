package EX_13072024;

public class Charcprogram {

    public static void main(String[] args) {
        char c = 'A';
        System.out.println(c);

        char c1 = '$';
        System.out.println(c1);

        char c2 = '\n';
        System.out.println(c2); //creates new line

        char c3 = '\u00C0';
        System.out.println(c3);
        char c4 = '\u00CA';
        System.out.println(c4);
        // :), :(, :D .;) -> unicode chars

    }
}
