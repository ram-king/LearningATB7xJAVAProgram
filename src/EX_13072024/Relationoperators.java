package EX_13072024;

public class Relationoperators {
    public static void main(String[] args) {
        // Relational Operators -> boolean
        //    // >, < >=,<=, == , != ( ! = ) - true or false.
        int age_durga = 31;
        int age_ram = 34;
        boolean result = age_ram == age_durga;
        System.out.println(result);
        boolean result2 = age_ram >= age_durga;
        System.out.println(result2);
        boolean result1 = age_ram <= age_durga;
        System.out.println(result1);
        boolean result3 = age_ram != age_durga;
        System.out.println(result3);
    }
}
